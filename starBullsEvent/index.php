<script>
    $(document).ready(function() {
        //change active
        $('.catalist li a').click(function() {
            $('.catalist').find(".active").removeClass();
            $(this).parent().addClass('active');

            fc = $(this).attr("data-filter");

            $(".item").each(function() {

                if (fc !== "all") {
                    if ($(this).attr("data-class") !== fc) {
                        $(this).css({
                            "display": "none"
                        });
                    } else {
                        $(this).css({
                            "display": "block"
                        });
                    }
                } else {
                    $(this).css({
                        "display": "block"
                    });
                }

            });
        })
    })

</script>

